describe('Order Checkout Test for Goldapple', () => {
    it('should successfully complete an order from the cart', () => {
        // Переход на главную страницу сайта
        cy.visit('https://goldapple.ru/');

        // Найти и кликнуть на первый товар в каталоге
        cy.get('.product-item').first().click(); // Замените селектор на актуальный, если необходимо

        // Добавить товар в корзину
        cy.get('.add-to-cart-button').click(); // Замените селектор на актуальный

        // Перейти в корзину
        cy.get('.cart-icon').click(); // Замените селектор на актуальный для иконки корзины

        // Проверка, что товар присутствует в корзине
        cy.get('.cart-item').should('have.length', 1); // Проверяем, что в корзине один товар

        // Проверка, что название товара соответствует ожидаемому
        cy.get('.cart-item').first().should('contain', 'Название товара'); // Замените текст на актуальный

        // Нажать кнопку "Оформить заказ"
        cy.get('.checkout-button').click(); // Замените селектор на актуальный

        // Заполнить форму оформления заказа (примерные поля)
        cy.get('input[name="name"]').type('Имя Фамилия'); // Замените на актуальные поля
        cy.get('input[name="email"]').type('example@example.com'); // Замените на актуальные поля
        cy.get('input[name="address"]').type('Адрес'); // Замените на актуальные поля
        cy.get('input[name="phone"]').type('1234567890'); // Замените на актуальные поля

        // Подтвердить оформление заказа
        cy.get('.confirm-order-button').click(); // Замените селектор на актуальный

        // Проверка, что отображается сообщение об успешном оформлении заказа
        cy.get('.order-confirmation-message') // Замените селектор на актуальный для сообщения
          .should('be.visible')
          .and('contain', 'Ваш заказ успешно оформлен'); // Замените текст на актуальный
    });
});
