describe('Favorite Products Test for Goldapple', () => {
    it('should add a product to favorites', () => {
        // Переход на главную страницу сайта
        cy.visit('https://goldapple.ru/');

        // Найти и кликнуть на первый товар в каталоге
        cy.get('.product-item').first().click(); // Замените селектор на актуальный, если необходимо

        // Проверка, что мы на странице товара
        cy.url().should('include', '/product/');

        // Нажать на кнопку "Добавить в избранное"
        cy.get('.add-to-favorites-button').click(); // Замените селектор на актуальный, если необходимо

        // Проверка, что товар был добавлен в избранное
        cy.get('.favorites-notification') // Замените селектор на актуальный для уведомления о добавлении
          .should('be.visible')
          .and('contain', 'Товар добавлен в избранное'); // Замените текст на актуальный

        // Перейти в раздел "Избранное"
        cy.get('.favorites-link').click(); // Замените селектор на актуальный

        // Проверка, что товар присутствует в списке избранных
        cy.get('.favorite-product-item') // Замените селектор на актуальный для товара в избранном
          .should('have.length', 1); // Проверяем, что в избранном один товар

        // Проверка, что добавленный товар соответствует ожидаемому
        cy.get('.favorite-product-item').first().should('contain', 'Название товара'); // Замените текст на актуальный
    });
});
